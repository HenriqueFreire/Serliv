// Tipos primitivos

// No JavaScript, temos os tipos primitivos e os tipos de referência.

// Os tipos primitivos são passados por valor, ou seja, quando você atribui um valor a uma variável, você está passando o valor da variável.
// Os tipos de referência são passados por referência, ou seja, quando você atribui um valor a uma variável, você está passando a referência da variável.

// Os tipos primitivos são:
// number
  let idade = 20;

// string
  let nome = 'João';

// boolean
  let isAdmin = true;

// null
  let multas = null;

// undefined
  let university = undefined;

// symbol
  let symbol = Symbol('symbol');