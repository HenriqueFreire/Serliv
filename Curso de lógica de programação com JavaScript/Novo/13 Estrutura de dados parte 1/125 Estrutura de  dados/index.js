// Pilha é uma estrutura de dados que segue a ordem LIFO (Last In, First Out), 
// ou seja, o último elemento a ser inserido é o primeiro a ser retirado.
// Exemplo de pilha
let pilha = []
pilha.push(1)
console.log(pilha)
pilha.push(2)
console.log(pilha)
pilha.push(3)
console.log(pilha)
pilha.pop()
console.log(pilha)
pilha.pop()
console.log(pilha)

// Fila é uma estrutura de dados que segue a ordem FIFO (First In, First Out),
// ou seja, o primeiro elemento a ser inserido é o primeiro a ser retirado.
// Exemplo de fila
let fila = []
fila.push(1)
console.log(fila)
fila.push(2)
console.log(fila)
fila.push(3)
console.log(fila)
fila.shift()
console.log(fila)
fila.shift()
console.log(fila)
