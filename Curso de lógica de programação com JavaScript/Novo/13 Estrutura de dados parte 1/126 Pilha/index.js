// Pilha é uma estrutura de dados que segue a ordem LIFO (Last In, First Out), 
// ou seja, o último elemento a ser inserido é o primeiro a ser retirado.
// Exemplo de pilha
let pilha = []
pilha.push(1)
console.log(pilha)
pilha.push(2)
console.log(pilha)
pilha.push(3)
console.log(pilha)
pilha.pop()
console.log(pilha)
pilha.pop()
console.log(pilha)